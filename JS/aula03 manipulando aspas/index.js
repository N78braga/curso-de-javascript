
// entendendo o uso das aspas em texto(String = Texto)
console.log('Ernando Braga'); 
console.log("Ernando Braga");
console.log(`Ernando Braga`);

/* para escreve um texto com aspas, tanto simple como duplas basta por dentro do texto 
as dupla ou simple comnforme o exemplo abixo. Todo os textos que é escriuto dentro das aspas
é considerado como String.
*/
console.log('Ernando "Braga"'); // imprimindo com aspas dupla.
console.log("Ernando 'Braga'"); // imprimindo com aspa simples.
console.log(`'Ernando' "Braga"`); // imprimindo com aspas simples com aspas duplas usar-se as crases

// entendendo como usar os números(Tipo: number = número)
console.log(123456); // basta só digitar dentro dos parentese número inteiro.
console.log(15.45); // número de ponto flutuante ou número decimal.

/* Observação dentro do console.log(); pode ser atribuido uma String ou um inteiro, decimal
tudo junto separado por uma virgula que ele vai respeita ccada tipo que estiver dentro
conforme o exemplo abaixo. 
*/
console.log("valor", 34, 63.50, 'A');

